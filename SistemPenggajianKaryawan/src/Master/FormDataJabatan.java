    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        if(editMode == false){
            JOptionPane.showMessageDialog(null, "Pilih data dari tabel dulu!");
            return;
        }
        try{
            String sql = "UPDATE jabatan SET nm_jabatan=?, gaji_pokok=? WHERE id_jabatan=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nm_jbtn.getText());
            stat.setString(2, gjpk.getText());
            stat.setString(3, id.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            editMode = false;
            id.setEditable(true);
            kosong();
            id.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah: "+e);
        }
    }//GEN-LAST:event_bubahActionPerformed

