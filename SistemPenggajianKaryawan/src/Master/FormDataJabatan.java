    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        if(id.getText().trim().equals("") ||
            nm_jbtn.getText().trim().equals("") ||
            gjpk.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Data harus diisi semua!");
            return;
        }
        // Validasi angka
        try{
            Double.parseDouble(gjpk.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Gaji Pokok harus berupa angka!");
            gjpk.requestFocus();
            return;
        }
        String sql = "insert into jabatan values (?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, id.getText());
            stat.setString(2, nm_jbtn.getText());
            stat.setString(3, gjpk.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            id.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
    }//GEN-LAST:event_bsimpanActionPerformed

