    protected void datatable(){
        Object[] Baris = {"ID KPI", "Nama KPI", "Skor Min", "Skor Max", "Bonus KPI"};
        tabmode = new DefaultTableModel(null, Baris);
        try{
            String sql = "SELECT * FROM tb_kpi ORDER BY id_kpi ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            DecimalFormatSymbols simbol = new DecimalFormatSymbols();
            simbol.setGroupingSeparator('.');
            simbol.setDecimalSeparator(',');
            DecimalFormat rupiah = new DecimalFormat("'Rp. '###,###,###.00", simbol);
            while(hasil.next()){
                double nominal = hasil.getDouble("bonus_kpi");
                String id = hasil.getString("id_kpi");
                String nama = hasil.getString("nama_kpi");
                int skorMin = hasil.getInt("skor_min");
                int skorMax = hasil.getInt("skor_max");
                String nominalFormat = rupiah.format(nominal);
                tabmode.addRow(new Object[]{
                    id,
                    nama,
                    skorMin,
                    skorMax,
                    nominalFormat
                });
            }
            tab.setModel(tabmode);
            hasil.close();
            stat.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil : " + e.getMessage());
        }
    }

