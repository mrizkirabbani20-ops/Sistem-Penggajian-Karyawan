    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        int min = Integer.parseInt(smin.getText());
        int max = Integer.parseInt(smax.getText());
        if(min > max){
            JOptionPane.showMessageDialog(null,
                "Skor Min tidak boleh lebih besar dari Skor Max");
            return;
        }
        if(id_kpi.getText().equals("") || nm_kpi.getText().equals("") || smin.getText().equals("")
                || smax.getText().equals("") || bonus.getText().equals("")){
             JOptionPane.showMessageDialog(null, "Data harus diisi semua");
             return;
         }
        String sql = "INSERT INTO tb_kpi VALUES (?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, id_kpi.getText());
            stat.setString(2, nm_kpi.getText());
            stat.setInt(3, Integer.parseInt(smin.getText()));
            stat.setInt(4, Integer.parseInt(smax.getText()));
            stat.setDouble(5, Double.parseDouble(bonus.getText()));
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            id_kpi.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

