    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        if(id_kpi.getText().equals("") ||
            nm_kpi.getText().equals("") ||
            smin.getText().equals("") ||
            smax.getText().equals("") ||
            bonus.getText().equals("")){
             JOptionPane.showMessageDialog(null, "Data harus diisi semua");
             return;
         }
        int min = Integer.parseInt(smin.getText());
        int max = Integer.parseInt(smax.getText());
        if(min > max){
            JOptionPane.showMessageDialog(null,
                "Skor Min tidak boleh lebih besar dari Skor Max");
            return;
        }
        try{
            String sql = "UPDATE tb_kpi SET "
                    + "nama_kpi=?, "
                    + "skor_min=?, "
                    + "skor_max=?, "
                    + "bonus_kpi=? "
                    + "WHERE id_kpi=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nm_kpi.getText());
            stat.setInt(2, Integer.parseInt(smin.getText()));
            stat.setInt(3, Integer.parseInt(smax.getText()));
            stat.setDouble(4, Double.parseDouble(bonus.getText()));
            stat.setString(5, id_kpi.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            kosong();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah : " + e.getMessage());
        }
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        int bar = tab.getSelectedRow();
        if(bar == -1){
            JOptionPane.showMessageDialog(null, "Pilih data dulu!");
            return;
        }
        int ok = JOptionPane.showConfirmDialog(null, 
                "Yakin ingin menghapus data?", 
                "Konfirmasi", 
                JOptionPane.YES_NO_OPTION);
        if(ok == JOptionPane.YES_OPTION){
            try{
                String id = tabmode.getValueAt(bar, 0).toString();
                String sql = "DELETE FROM tb_kpi WHERE id_kpi=?";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, id);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                kosong();
                id_kpi.requestFocus();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapusActionPerformed

