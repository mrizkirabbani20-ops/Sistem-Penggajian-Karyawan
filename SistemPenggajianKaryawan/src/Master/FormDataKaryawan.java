    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        String jenis = null;
        if(lkariawan.isSelected()){
            jenis = "Laki-Laki";
        }else if(pkariawan.isSelected()){
            jenis = "Perempuan";
        }
        if(editMode == false){
        JOptionPane.showMessageDialog(null, "Pilih data dari tabel dulu!");
        return;
        }
        try{
            String sql = "UPDATE karyawan SET nm_karyawan=?, jk=?, alamat=?, telp=?, id_jabatan=? WHERE id_karyawan=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nmk.getText());
            stat.setString(2, jenis);
            stat.setString(3, ak.getText());
            stat.setString(4, ntk.getText());
            String idJabatan = getIdJabatan(jabatan.getSelectedItem().toString());
            stat.setString(5, idJabatan);
            stat.setString(6, idk.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            editMode = false;
            idk.setEditable(true);
            kosong();
            idk.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah: "+e);
        }
    }//GEN-LAST:event_bubahActionPerformed

