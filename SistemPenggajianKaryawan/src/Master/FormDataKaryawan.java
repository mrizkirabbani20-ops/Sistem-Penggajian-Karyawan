    public void setData(String id, String nama, String jk, String alamat, String telp, String jab){
        idk.setText(id);
        nmk.setText(nama);
        if(jk.equals("Laki-Laki")){
            lkariawan.setSelected(true);
        } else {
            pkariawan.setSelected(true);
        }
        ak.setText(alamat);
        ntk.setText(telp);
        jabatan.setSelectedItem(jab);
        idk.setEditable(false);
        editMode = true;
    }
    
    private void tampilJabatan() {
        try {
            Statement st = conn.createStatement();
            String sql = "SELECT * FROM jabatan";
            ResultSet rs = st.executeQuery(sql);
            jabatan.removeAllItems();
            while (rs.next()) {
                jabatan.addItem(rs.getString("nm_jabatan"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Gagal menampilkan data jabatan : " + e.getMessage());
        }
    }
    
    private String getIdJabatan(String namaJabatan){
        String id = "";
        try{
            String sql = "SELECT id_jabatan FROM jabatan WHERE nm_jabatan=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, namaJabatan);
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                id = rs.getString("id_jabatan");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        return id;
    }
    
