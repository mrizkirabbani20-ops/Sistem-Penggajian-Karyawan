    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // Validasi input kosong
        if(idk.getText().trim().equals("") ||
           nmk.getText().trim().equals("") ||
           ak.getText().trim().equals("") ||
           ntk.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Data harus diisi semua!");
            return;
        }
        // Validasi jenis kelamin
        if(!lkariawan.isSelected() && !pkariawan.isSelected()){
            JOptionPane.showMessageDialog(null, "Pilih jenis kelamin terlebih dahulu!");
            return;
        }
        // Validasi jabatan
        if(jabatan.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Pilih jabatan terlebih dahulu!");
            return;
        }
        // Validasi No Telepon (Harus Angka)
        if(!ntk.getText().matches("[0-9]+")){
            JOptionPane.showMessageDialog(null,
                    "Nomor telepon hanya boleh angka!");
            ntk.requestFocus();
            return;
        }
        String jenis = null;
        if(lkariawan.isSelected()){
            jenis = "Laki-Laki";
        }else if(pkariawan.isSelected()){
            jenis = "Perempuan";
        }
        String sql = "insert into karyawan values (?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, idk.getText());
            stat.setString(2, nmk.getText());
            stat.setString(3, jenis);
            stat.setString(4, ak.getText());
            stat.setString(5, ntk.getText());
            String idJabatan = getIdJabatan(jabatan.getSelectedItem().toString());
            stat.setString(6, idJabatan);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            idk.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
               
    }//GEN-LAST:event_bsimpanActionPerformed

