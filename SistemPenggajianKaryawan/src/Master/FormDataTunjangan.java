    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // Validasi input kosong
        if (idtj.getText().isEmpty() ||
            nmtj.getText().isEmpty() ||
            nml.getText().isEmpty() ||
            ket.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data harus diisi semua!");
            return;
        }
        // Validasi nominal harus angka
        try {
            Long.parseLong(nml.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nominal harus angka!");
            return;
        }
        String sql = "insert into tunjangan values (?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, idtj.getText());
            stat.setString(2, nmtj.getText());
            stat.setString(3, nml.getText());
            stat.setString(4, ket.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            idtj.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
    }//GEN-LAST:event_bsimpanActionPerformed

