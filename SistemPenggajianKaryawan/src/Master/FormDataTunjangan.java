    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        if(editMode == false){
            JOptionPane.showMessageDialog(null, "Pilih data dari tabel dulu!");
            return;
        }
        try {
            Long.parseLong(nml.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nominal harus angka!");
            return;
        }
        try{
            String sql = "UPDATE tunjangan SET nama_tunjangan=?, nominal=?, keterangan=? WHERE id_tunjangan=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nmtj.getText());
            stat.setString(2, nml.getText());
            stat.setString(3, ket.getText());
            stat.setString(4, idtj.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            editMode = false;
            idtj.setEditable(true);
            kosong();
            idtj.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah: "+e);
        }
    }//GEN-LAST:event_bubahActionPerformed

