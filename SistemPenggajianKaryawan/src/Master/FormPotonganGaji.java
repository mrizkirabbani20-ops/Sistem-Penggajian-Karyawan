    protected void datatable(){
        Object[] Baris = {"ID Potongan", "Jenis Potongan", "Nominal"};
        tabmode = new DefaultTableModel(null, Baris);
        try{
            String sql = "SELECT * FROM tb_potongan ORDER BY id_potongan ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            DecimalFormatSymbols simbol = new DecimalFormatSymbols();
            simbol.setGroupingSeparator('.');
            simbol.setDecimalSeparator(',');
            DecimalFormat rupiah = new DecimalFormat("'Rp. '###,###,###.00", simbol);
            while(hasil.next()){
                double nominal = hasil.getDouble("nominal_potongan");
                String id = hasil.getString("id_potongan");
                String jenis = hasil.getString("jenis_potongan");
                String nominalFormat = rupiah.format(nominal);
                tabmode.addRow(new Object[]{
                    id,
                    jenis,
                    nominalFormat
                });
            }
            tablep.setModel(tabmode);
            hasil.close();
            stat.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil : " + e.getMessage());
        }
    }

