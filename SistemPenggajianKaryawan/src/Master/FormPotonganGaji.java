    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        try{
            String sql = "UPDATE tb_potongan SET "
                    + "jenis_potongan=?, "
                    + "nominal_potongan=? "
                    + "WHERE id_potongan=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, jenisp.getText());
            stat.setString(2, nominal.getText());
            stat.setString(3, idp.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            kosong();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah : " + e.getMessage());
        }
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        int bar = tablep.getSelectedRow();
        if(bar == -1){
            JOptionPane.showMessageDialog(null, "Pilih data dulu!");
            return;
        }
        int ok = JOptionPane.showConfirmDialog(null, 
                "Yakin ingin menghapus data?", 
                "Konfirmasi", 
                JOptionPane.YES_NO_OPTION);
        if(ok == JOptionPane.YES_OPTION){
            try{
                String id = tabmode.getValueAt(bar, 0).toString();
                String sql = "DELETE FROM tb_potongan WHERE id_potongan=?";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, id);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                kosong();
                idp.requestFocus();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapusActionPerformed

    private void tablepMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablepMouseClicked
        int bar = tablep.getSelectedRow();
        String id = tabmode.getValueAt(bar, 0).toString();
        String jenis = tabmode.getValueAt(bar, 1).toString();
        String rupiah = tabmode.getValueAt(bar, 2).toString();
        String nominalClean = rupiah
                .replace("Rp.", "")
                .replace(".", "")
                .replace(",00", "")
                .trim();
        idp.setText(id);
        jenisp.setText(jenis);
        nominal.setText(nominalClean);
    }//GEN-LAST:event_tablepMouseClicked

