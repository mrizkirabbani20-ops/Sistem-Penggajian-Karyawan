    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        int bar = tbt.getSelectedRow();
        // cek apakah ada data yang dipilih
        if(bar == -1){
            JOptionPane.showMessageDialog(null, "Pilih data dulu!");
            return;
        }
        // konfirmasi sebelum hapus
        int ok = JOptionPane.showConfirmDialog(null, 
                "Yakin ingin menghapus data?", 
                "Konfirmasi", 
                JOptionPane.YES_NO_OPTION);
        if(ok == JOptionPane.YES_OPTION){
            try{
                // ambil ID dari tabel
                String id = tabmode.getValueAt(bar, 0).toString();
                // query hapus
                String sql = "DELETE FROM tunjangan WHERE id_tunjangan=?";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, id);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                datatable(); // refresh tabel
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + e);
            }
        }
    }//GEN-LAST:event_bhapusActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        int bar = tbt.getSelectedRow();
        if(bar == -1){
            JOptionPane.showMessageDialog(null, "Pilih data dulu!");
            return;
        }
        try{
            String id = tabmode.getValueAt(bar, 0).toString();
            String nama = tabmode.getValueAt(bar, 1).toString();
            String nominal = tabmode.getValueAt(bar, 2).toString();
            String ket = tabmode.getValueAt (bar, 3).toString();
            FormDataTunjangan form = new FormDataTunjangan();
            form.setData(id, nama, nominal, ket);
            form.setVisible(true);
            datatable();
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_bubahActionPerformed

