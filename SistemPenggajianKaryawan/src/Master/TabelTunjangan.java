    protected void datatable(){
        Object[] Baris = {"ID Tunjangan","Nama Tunjangan","Nominal", "Keterangan"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = cari.getText();

        try{
            String sql = "SELECT * FROM tunjangan WHERE id_tunjangan LIKE ? OR nama_tunjangan LIKE ?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, "%" + cariitem + "%");
            stat.setString(2, "%" + cariitem + "%");
            ResultSet hasil = stat.executeQuery();
            DecimalFormatSymbols simbol = new DecimalFormatSymbols();
            simbol.setCurrencySymbol("Rp. ");
            simbol.setGroupingSeparator('.');
            simbol.setDecimalSeparator(',');
            DecimalFormat rupiah = new DecimalFormat("'Rp. '###,###,###.00", simbol);
            while (hasil.next()) {
                double nominal = hasil.getDouble("nominal");
                tabmode.addRow(new Object[]{
                    hasil.getString("id_tunjangan"),
                    hasil.getString("nama_tunjangan"),
                    rupiah.format(nominal),
                    hasil.getString("keterangan")
                });
            }
            tbt.setModel(tabmode);
            hasil.close();
            stat.close();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil"+e);
        }
    }
